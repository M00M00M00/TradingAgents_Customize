import time
import json


def create_research_manager(llm, memory):
    def research_manager_node(state) -> dict:
        history = state["investment_debate_state"].get("history", "")
        market_research_report = state["market_report"]
        sentiment_report = state["sentiment_report"]
        news_report = state["news_report"]
        fundamentals_report = state["fundamentals_report"]

        investment_debate_state = state["investment_debate_state"]

        curr_situation = f"{market_research_report}\n\n{sentiment_report}\n\n{news_report}\n\n{fundamentals_report}"
        past_memories = memory.get_memories(curr_situation, n_matches=2)

        past_memory_str = ""
        for i, rec in enumerate(past_memories, 1):
            past_memory_str += rec["recommendation"] + "\n\n"

        prompt = f"""As the portfolio manager and debate facilitator, make a definitive decision: LONG, SHORT, or NEUTRAL. Choose NEUTRAL only if clearly justified by low confluence; otherwise pick a side.

Summarize bull vs bear briefly, then give:
- Recommendation (LONG/SHORT/NEUTRAL) with key evidence (HTF bias, pattern/level, momentum, order flow).
- Strategic actions: entry idea, invalidation/SL zone (~0.5-1% risk), TP zone with RR target (1.5-2.5 preferred, 1-10 allowed).
- Lessons: incorporate past mistakes here:
\"{past_memory_str}\"

Debate History:
{history}"""
        response = llm.invoke(prompt)

        new_investment_debate_state = {
            "judge_decision": response.content,
            "history": investment_debate_state.get("history", ""),
            "bear_history": investment_debate_state.get("bear_history", ""),
            "bull_history": investment_debate_state.get("bull_history", ""),
            "current_response": response.content,
            "count": investment_debate_state["count"],
        }

        return {
            "investment_debate_state": new_investment_debate_state,
            "investment_plan": response.content,
        }

    return research_manager_node
